function seed() {
  console.log("Starting Navarro County restaurant seed...\n");
  console.log("=== Adding Restaurants ===");

  let added = 0;
  let skipped = 0;

  restaurants.forEach(restaurant => {
    const existing = db.prepare(`
      SELECT id FROM entities WHERE entity_type = 'Restaurant' AND json_extract(data, '$.name') = ?
    `).get(restaurant.name);

    if (existing) {
      console.log(`  [SKIP] ${restaurant.name}`);
      skipped++;
    } else {
      insertRestaurant(restaurant);
      added++;
    }
  });

  console.log(`\n=== Seed Complete ===`);
  console.log(`Added: ${added} restaurants`);
  console.log(`Skipped: ${skipped} (already exist)`);
  console.log(`Total in database: ${added + skipped}`);
}

seed();
db.close();
